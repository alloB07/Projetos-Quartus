// Posições dos sprites
wire [9:0] dino_y;
assign dino_y = DINO_Y - topo;
wire [3:0] dinoX;
assign dinoX = hcounter - DINO_X;
wire [3:0] dinoY;
assign dinoY = vcounter - dino_y;
wire [3:0] cactoY;
assign cactoY = vcounter - CACTO_Y;
wire [3:0] cactoX;
assign cactoX = hcounter - cacto;
wire [3:0] passaroX;
assign passaroX = hcounter - passarin;
wire [3:0] passaroY;
assign passaroY = vcounter - PASSARO_Y;
wire [3:0] pontuacaoX;
assign pontuacaoX = hcounter - PONTUACAO_X;
wire [3:0] pontuacaoY;
assign pontuacaoY = vcounter - PONTUACAO_Y;
	 
always @(posedge clk) begin

	if (dia) begin 
		red <= 0; green <= 0; blue <= 0;
	end
	else begin
		red <= 1; green <= 1; blue <= 1;
	end
	
	if (hcounter[9] == 1) begin
		frame <= frame + 1;
	end

	// Pontuação primeiro dígito
	if (hcounter >= PONTUACAO_X && hcounter < PONTUACAO_X + 16 &&
		vcounter >= PONTUACAO_Y && vcounter < PONTUACAO_Y + 16) begin
		if (num[pontuacaoY][pontuacao / 10][15 - pontuacaoX] == 1) begin
			if (dia) begin 
				red <= 1; green <= 1; blue <= 1;
			end
			else begin
				red <= 0; green <= 0; blue <= 0;
			end
		end
		else begin
			if (dia) begin 
				red <= 0; green <= 0; blue <= 0;
			end
			else begin
				red <= 1; green <= 1; blue <= 1;
			end
		end
	end

	// Pontuação segundo dígito
	if (hcounter >= PONTUACAO_X + 18 && hcounter < PONTUACAO_X + 34 &&
		vcounter >= PONTUACAO_Y && vcounter < PONTUACAO_Y + 16) begin
		if (num[pontuacaoY][pontuacao % 10][15 - pontuacaoX + 18] == 1) begin
			if (dia) begin 
				red <= 1; green <= 1; blue <= 1;
			end
			else begin
				red <= 0; green <= 0; blue <= 0;
			end
		end
		else begin
			if (dia) begin 
				red <= 0; green <= 0; blue <= 0;
			end
			else begin
				red <= 1; green <= 1; blue <= 1;
			end
		end
	end
	
    // Dinossauro
    if (hcounter >= DINO_X && hcounter < DINO_X + 16 &&
        vcounter >= dino_y && vcounter < dino_y + 16)
    begin
        if (batebate ? dino0[dinoY][dinoX] == 1 : frame[20] ? dino0[dinoY][dinoX] == 1 : dino1[dinoY][dinoX] == 1) begin
            if (dia) begin 
					red <= 1; green <= 1; blue <= 1;
				end
				else begin
					red <= 0; green <= 0; blue <= 0;
				end
			end
			else begin
				if (dia) begin 
					red <= 0; green <= 0; blue <= 0;
				end
				else begin
					red <= 1; green <= 1; blue <= 1;
				end
        end
    end

    // Cacto
	if (vcounter >= CACTO_Y && vcounter < CACTO_Y + 16 &&
		 hcounter >= cacto && hcounter < cacto + 16)
	begin
		if (cacto0[cactoY][cactoX] == 1) begin
			if (dia) begin 
				red <= 1; green <= 1; blue <= 1;
			end
			else begin
				red <= 0; green <= 0; blue <= 0;
			end
		end
		else begin
			if (dia) begin 
				red <= 0; green <= 0; blue <= 0;
			end
			else begin
				red <= 1; green <= 1; blue <= 1;
			end
		end
	end
	
	// Pássaro
	if (vcounter >= PASSARO_Y && vcounter < PASSARO_Y + 16 &&
		 hcounter >= passarin && hcounter < passarin + 16)
	begin
		if (batebate ? voa0[passaroY][passaroX] == 1 : frame[21] ? voa0[passaroY][passaroX] == 1 : voa1[passaroX][passaroY] == 1) begin
			if (dia) begin 
				red <= 1; green <= 1; blue <= 1;
			end
			else begin
				red <= 0; green <= 0; blue <= 0;
			end
		end
		else begin
			if (dia) begin 
				red <= 0; green <= 0; blue <= 0;
			end
			else begin
				red <= 1; green <= 1; blue <= 1;
			end
		end
	end
	
	// Chão
    if (vcounter == 450) begin
        if (dia) begin 
				red <= 1; green <= 1; blue <= 1;
			end
			else begin
				red <= 0; green <= 0; blue <= 0;
			end
    end

end

